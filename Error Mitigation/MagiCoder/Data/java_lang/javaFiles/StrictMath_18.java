import java.lang.StrictMath;

public class StrictMath_18 {
    public static void main(String[] args) {
        double angleInRadians = 1.57; // you can replace this with any angle in radians
        double result = StrictMath.tan(angleInRadians);
        System.out.println("The tangent of " + angleInRadians + " radians is " + result);
    }
}
