import java.lang.StrictMath;

public class StrictMath_12 {
    public static void main(String[] args) {
        double base = 2.0;
        double exponent = 3.0;
        double result = StrictMath.pow(base, exponent);
        System.out.println(base + " raised to the power of " + exponent + " is: " + result);
    }
}
