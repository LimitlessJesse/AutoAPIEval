import java.lang.Math;

public class Math_3 {
    public static void main(String[] args) {
        double a = 9.2;
        double b = -4.8;
        double c = 0.0;
        double d = Double.NaN;
        double e = Double.POSITIVE_INFINITY;
        double f = Double.NEGATIVE_INFINITY;

        System.out.println("Math.floor(" + a + ") = " + Math.floor(a));
        System.out.println("Math.floor(" + b + ") = " + Math.floor(b));
        System.out.println("Math.floor(" + c + ") = " + Math.floor(c));
        System.out.println("Math.floor(" + d + ") = " + Math.floor(d));
        System.out.println("Math.floor(" + e + ") = " + Math.floor(e));
        System.out.println("Math.floor(" + f + ") = " + Math.floor(f));
    }
}
