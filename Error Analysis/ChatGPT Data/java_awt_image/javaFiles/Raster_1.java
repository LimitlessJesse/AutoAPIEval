import java.awt.image.Raster;

public class Raster_1 {
    public static void main(String[] args) {
        int width = 0;
        Raster raster = null; // Initialize Raster object
        if (raster != null) {
            width = raster.getWidth();
        }
        System.out.println("Width: " + width);
    }
}
